<template>
  <section class="app-main">
    <transition name="fade-transform" mode="out-in">
      <!-- keep-alive保证当前组件被缓存 -->
      <keep-alive :include="cachedViews">
        <!-- 所有自己写的组件最终都是替换<app-main />里面的<router-view/>， -->
        <!-- 比如dashboard组件就是替换的此处，在页面上展示出来就是右下角那一大块区域 -->
